public class Public
{
	public static class Inner1
	{
		public void talk()
		{	System.out.println( "In Public.Inner1.talk" ) ; }

	} // class Inner1

	public static class Inner2
	{
		public void talk()
		{	System.out.println( "In Public.Inner2.talk" ) ; }

	} // class Inner1
} // class Public

